----------- SELECTS -------------

SELECT MAX(FEVEREIRO) AS MAIOR_FEV FROM VENDEDORES;
SELECT MIN(FEVEREIRO) AS MENOR_FEV FROM VENDEDORES;
SELECT AVG(FEVEREIRO) AS MEDIA_FEV FROM VENDEDORES;


SELECT 
    MAX(JANEIRO) AS MAX_JAN,
    MIN(JANEIRO) AS MIN_JAN,
    AVG(JANEIRO) AS AVG_JAN,
    MAX(FEVEREIRO) AS MAX_FEV,
    MIN(FEVEREIRO) AS MIN_FEV,
    AVG(FEVEREIRO) AS AVG_FEV,
    MAX(MARCO) AS MAX_MAR,
    MIN(MARCO) AS MIN_MAR,
    AVG(MARCO) AS AVG_MAR
FROM VENDEDORES;


SELECT 
    MAX(JANEIRO) AS MAX_JAN,
    MIN(JANEIRO) AS MIN_JAN,
    TRUNCATE(AVG(JANEIRO), 2) AS AVG_JAN
FROM VENDEDORES;


SELECT SUM(JANEIRO) AS TOTAL_JAN FROM VENDEDORES;
SELECT 
    SUM(JANEIRO) AS TOTAL_JAN,
    SUM(FEVEREIRO) AS TOTAL_FEV,
    SUM(MARCO) AS TOTAL_MAR
FROM VENDEDORES;


SELECT SEXO, SUM(JANEIRO) AS TOTAL_JAN
FROM VENDEDORES
GROUP BY SEXO;


SELECT NOME, MARCO 
FROM VENDEDORES
WHERE MARCO = (SELECT MIN(MARCO) FROM VENDEDORES);


SELECT NOME, MARCO 
FROM VENDEDORES
WHERE MARCO = (SELECT MAX(MARCO) FROM VENDEDORES);


SELECT NOME, FEVEREIRO 
FROM VENDEDORES
WHERE FEVEREIRO > (SELECT AVG(FEVEREIRO) FROM VENDEDORES);


SELECT AVG(FEVEREIRO) AS MEDIA_FEV FROM VENDEDORES;


SELECT 
    NOME,
    JANEIRO,
    FEVEREIRO,
    MARCO,
    (JANEIRO + FEVEREIRO + MARCO) AS TOTAL,
    TRUNCATE((JANEIRO + FEVEREIRO + MARCO) / 3, 2) AS MEDIA
FROM VENDEDORES;


SELECT 
    NOME,
    JANEIRO,
    FEVEREIRO,
    MARCO,
    (JANEIRO + FEVEREIRO + MARCO) AS TOTAL,
    (JANEIRO + FEVEREIRO + MARCO) * 0.25 AS BONUS_25_PORCENTO,
    TRUNCATE((JANEIRO + FEVEREIRO + MARCO) / 3, 2) AS MEDIA
FROM VENDEDORES;
